// Routes

// "/" => Hi
app.get("/", function (req, res){

  res.send("Hi there!");

});


// "/bye" => Goodbye
app.get("/bye", function (req, res) {

  res.send("Bye bye...");

});


// "/dog" => Meow!!
app.get("/dog", function (req, res) {
  
  res.send("Meow!");

});


