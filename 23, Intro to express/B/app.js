// ROUTES


// "/" => Hi
app.get("/", function (req, res){

  res.send("Hi there, welcome to my assignment!");

});





// Speak -> Animal sounds
app.get("/speak/:animal", function (req, res) {


  var animal = req.params.animal;

  var sendString = "";


  switch (animal) {

  case "pig":

    sendString = "The pig says 'Oink'";

    break;


  case "cow":

    sendString = "The cow says 'Moo'";

    break;


  case "dog":

    sendString = "The dog says 'Woof Woof'";

    break;


  default:

    sendString = "Animal unknown, try again";

  }


  res.send(sendString);


});





// Repeat -> Repeat string x times
app.get("/repeat/:string/:times/:tocase", function (req, res) {


  var repeatString = req.params.string;
  var repeatTimes = req.params.times;
  var toCase = req.params.tocase;

  var sendString = "";


  for (var index = repeatTimes; index > 0; index--) {

    sendString = sendString + repeatString + " ";

  }


  if (toCase === "upper") {

    sendString = sendString.trim();
    sendString = sendString.toUpperCase();

  } else {

    sendString = sendString.trim();

  }


  res.send(sendString);


});





// Any other route
